// Get franchises by status
export const getFranchisesByStatus = (status?: string): EnhancedFranchise[] => {
  if (!status || status === 'all') return allSampleFranchises;
  return allSampleFranchises.filter(franchise => franchise.status === status);
};

// Get a franchise by ID
export const getFranchiseById = (id: string): EnhancedFranchise | undefined => {
  return allSampleFranchises.find(franchise => franchise.id === id);
};
